package com.sayho.workoutcalendar.model;

public class Workout {
	private String woDiv;
	private String woCod;
	private String woNm;
	public String getWoDiv() {
		return woDiv;
	}
	public void setWoDiv(String woDiv) {
		this.woDiv = woDiv;
	}
	public String getWoCod() {
		return woCod;
	}
	public void setWoCod(String woCod) {
		this.woCod = woCod;
	}
	public String getWoNm() {
		return woNm;
	}
	public void setWoNm(String woNm) {
		this.woNm = woNm;
	}
}
